    /**
     * 分页获取图片列表（封装类，加入缓存）
     */
    @Deprecated // 弃用
    @PostMapping("/list/page/vo/cache")
    public BaseResponse<Page<PictureVO>> listPictureVOByPageWithCaChe(@RequestBody PictureQueryRequest pictureQueryRequest,
                                                                      HttpServletRequest request) {

        long current = pictureQueryRequest.getCurrent();
        long size = pictureQueryRequest.getPageSize();
        // 限制爬虫
        ThrowUtils.throwIf(size > 20, ErrorCode.PARAMS_ERROR);
        // 普通用户只能查询审核通过的图片
        pictureQueryRequest.setReviewStatus(PictureReviewStatusEnum.PASS.getValue());

        // 缓存逻辑
        // 查询缓存，无数据再查询数据库
        String queryCondition = JSONUtil.toJsonStr(pictureQueryRequest); // 将查询请求体转为 json 字符串 提高可读性
        String hashKey = DigestUtils.md5DigestAsHex(queryCondition.getBytes()); // 使用md5算法加密压缩字符串
        String cacheKey = String.format("yunpicture:listPictureVOByPage:%s", hashKey); // 缓存的 key

        // 多级缓存：本地缓存 + Redis缓存
        // 1. 先从本地缓存中查询数据
        String cachedValue = LOCAL_CACHE.getIfPresent(cacheKey);
        if (cachedValue != null) {
            // 如果缓存命中，则直接返回缓存数据
            Page<PictureVO> cachedPage = JSONUtil.toBean(cachedValue, Page.class); // 将json字符串转为对象
            return ResultUtils.success(cachedPage);
        }

        // 2. 本地缓存未命中时，操作Redis缓存,从缓存中查询数据
        ValueOperations<String, String> opsForValue = stringRedisTemplate.opsForValue(); // 获取Redis操作对象
        cachedValue = opsForValue.get(cacheKey); // 从缓存中获取数据
        if (cachedValue != null) {
            // 如果缓存命中，更新本地缓存，并返回返回缓存数据
            LOCAL_CACHE.put(cacheKey, cachedValue);
            Page<PictureVO> cachedPage = JSONUtil.toBean(cachedValue, Page.class); // 将json字符串转为对象
            return ResultUtils.success(cachedPage);
        }

        // 3. 查询数据库（当缓存没有查询到数据时）
        Page<Picture> picturePage = pictureService.page(new Page<>(current, size),
                pictureService.getQueryWrapper(pictureQueryRequest));
        Page<PictureVO> pictureVOPage = pictureService.getPictureVOPage(picturePage, request); // 获取封装类
        // 4. 更新缓存
        // 更新redis缓存
        String cacheValue = JSONUtil.toJsonStr(pictureVOPage); // 数据值
        // 设置过期时间，5 - 10 分钟，设置区间随机过期时间避免缓存雪崩
        int cacheTime = 300 + RandomUtil.randomInt(0, 300); // 300 - 600 秒间的随机过期时间
        opsForValue.set(cacheKey, cacheValue, cacheTime, TimeUnit.SECONDS);
        // 更新本地缓存，无需指定时间，已在构造缓存时就指定好了
        LOCAL_CACHE.put(cacheKey, cacheValue);
        // 获取封装类
        return ResultUtils.success(pictureVOPage);
    }

